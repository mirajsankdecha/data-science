# Age Category Program in Python

age = int(input("Enter your Age : ")) 

if age >= 13 and age <= 19 : 
    print("Your are Teen") 
elif age >= 2 and age <=4 :
    print("You are Toddler")
elif age >= 5 and age <= 12 :
    print("You are Child")
elif age >= 20 and age <= 39 :
    print("You are Adult")          
else :
    print("You are Elder") 

