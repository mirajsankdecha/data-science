new_tuple = (1, 2, 3, 4, 5)
print(new_tuple)  # This will print the tuple
print(new_tuple[0])  # This will print the first element
print(new_tuple[1:3])  # This will print the second and third element   
print(new_tuple[-1])  # This will print the last element
print(new_tuple[-3:-1])  # This will print the third and fourth element
print(new_tuple[0:5:2])  # This will print the first, third and fifth element
for ele in new_tuple:
    print(ele)  # This will print all the elements in the tuple